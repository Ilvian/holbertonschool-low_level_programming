/**
 *reverse_array -  function that reverses the content of an array of integers
 *@a: pointer to array
 *@n: n to be reversed
*/
void reverse_array(int *a, int n)
{
	int length = 0;
	int reverse;

	while (a[length] != '\0')
		length++;
	while (a[reverse] = a[length])
		reverse--;
}
